print("This is called advance function concept in Python. We use input, return, output...")
def plus(num1, num2):
    add = num1 + num2
    return(add)
def minus(num1, num2):
    sub = num1 - num2
    return(sub)
def multipl(num1, num2):
    mul = num1 * num2
    return(mul)

print(plus(5,10))
print(minus(5,10))
print(multipl(5,10))
